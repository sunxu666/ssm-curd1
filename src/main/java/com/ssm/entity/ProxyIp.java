package com.ssm.entity;

/**
 * 获取id和端口号
 */
public class ProxyIp {
    private int id;

   private String ip;

   private Integer port;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getIp() {
        return ip;
    }

    public void setIp(String ip) {
        this.ip = ip;
    }

    public Integer getPort() {
        return port;
    }

    public void setPort(Integer port) {
        this.port = port;
    }
}
